def headerify(text):
    lines = [list() for x in range(12)]
    for character in text:
        charlines = asciibet[character].splitlines()
        for (charline, line) in zip(charlines, lines):
            line.append(charline)
    lines = [''.join(line) for line in lines]
    lines = '\n'.join(lines)
    return lines

